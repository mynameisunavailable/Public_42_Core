#include "../ft_printf.h"

char	*ft_ldtoa(long long n)
{
	int		i;
	char	string[21];
	int		negative;

	negative = 1;
	i = 0;
	if (n < 0)
		negative = -1;
	while (n <= -10 || 10 <= n)
	{
		string[i++] = '0' + (negative * (n % 10));
		n = n / 10;
	}
	string[i++] = '0' + (negative * (n % 10));
	if (negative == -1)
		string[i++] = '-';
	string[i] = '\0';
	return (ft_reverse_string_malloc(string));
}
