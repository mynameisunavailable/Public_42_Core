#include "../ft_printf.h"

void	ft_reverse_string(char *string, char *reverse_string, int length)
{
	int	index;

	index = 0;
	while (length > 0)
	{
		reverse_string[index] = string[length - 1];
		length--;
		index++;
	}
	reverse_string[index] = '\0';
}
