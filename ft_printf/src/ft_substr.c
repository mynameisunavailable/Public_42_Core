#include "../ft_printf.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*res;

	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		len = 0;
	res = ft_strndup(s + start, len);
	return (res);
}
