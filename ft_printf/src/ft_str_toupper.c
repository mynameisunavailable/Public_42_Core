#include "../ft_printf.h"

char	*ft_str_toupper(char *str)
{
	char	*str_start;

	str_start = str;
	while (*str)
	{
		if ('a' <= *str && *str <= 'z')
			*str = *str - 32;
		str++;
	}
	return (str_start);
}
