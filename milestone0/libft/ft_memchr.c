#include <stdlib.h>

void	*ft_memchr(const void *src, int c, size_t size)
{
	unsigned char	*s;

	s = (unsigned char *)src;
	while (size > 0)
	{
		if (*s == (unsigned char)c)
		{
			return ((void *)s);
		}
		s++;
		size--;
	}
	return (NULL);
}
