#include <stdlib.h>

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	char	*d;
	size_t	i;

	d = (char *)dest;
	i = 0;
	if (d < (char *)src)
	{
		while (n > i)
		{
			d[i] = ((char *)src)[i];
			i++;
		}
	}
	else
	{
		while (n > 0)
		{
			n--;
			d[n] = ((char *)src)[n];
		}
	}
	return (dest);
}
