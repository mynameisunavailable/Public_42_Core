#include "libft.h"
#include <stdlib.h>

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	if (*lst == NULL)
	{
		ft_lstadd_front(lst, new);
		return ;
	}
	else
	{
		temp = ft_lstlast(*lst);
		(temp)->next = new;
	}
}
