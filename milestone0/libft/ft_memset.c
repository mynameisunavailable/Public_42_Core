#include <stdlib.h>

void	*ft_memset(void *dest, int n, size_t size)
{
	size_t			i;
	unsigned char	*d;

	i = 0;
	d = (unsigned char *)dest;
	while (i < size)
	{
		d[i] = (unsigned char)n;
		i++;
	}
	return (dest);
}
