#include <stdlib.h>

void	*ft_bzero(void *dest, size_t size)
{
	size_t			i;
	unsigned char	*d;

	i = 0;
	d = (unsigned char *)dest;
	while (i < size)
	{
		d[i] = 0;
		i++;
	}
	return (dest);
}
