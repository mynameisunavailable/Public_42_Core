#include <stddef.h>

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	count;

	count = 0;
	while (size > 0 && *dest)
	{
		dest++;
		count++;
		size--;
	}
	while (size > 1 && *src)
	{
		*dest++ = *src++;
		count++;
		size--;
	}
	while (*src++)
		count++;
	if (size > 0)
		*dest = '\0';
	return (count);
}
