#include <stdlib.h>

void	*ft_memcpy(void *dest, const void *src, size_t size)
{
	char	*d;
	char	*s;

	d = (char *)dest;
	s = (char *)src;
	while (size > 0)
	{
		*d++ = *s++;
		size--;
	}
	return (dest);
}
