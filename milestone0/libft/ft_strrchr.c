#include "libft.h"

char	*ft_strrchr(const char *str, int to_find)
{
	int	i;

	i = (int)ft_strlen(str);
	while (i >= 0)
	{
		if ((unsigned char)str[i] == (unsigned char)to_find)
			return ((char *)str + i);
		i--;
	}
	return (NULL);
}
