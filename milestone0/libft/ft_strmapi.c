#include <stdlib.h>
#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*res;
	char	a;
	int		i;

	if (!s || !f)
		return (NULL);
	res = ft_strdup(s);
	if (!res)
		return (NULL);
	i = 0;
	while (*s)
	{
		a = *s;
		res[i] = f(i, a);
		s++;
		i++;
	}
	return (res);
}
